#include <iostream>

using namespace std;

int main()
{
    int num = 10;
    float floatNum = 3.14;
    char letter = 'A';
    string text = "Lorem ipsum dolor sit amet";
    bool myBoolean = true;

    cout << "Number: " << num << endl;
    cout << "Floating Number: " << floatNum << endl;
    cout << "Letter: " << letter << endl;
    cout << "Text: " << text << endl;
    cout << "Boolean: " << myBoolean << endl;

    return 0;
}
